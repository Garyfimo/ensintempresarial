public class Piso {

    int idPiso;
    String numPiso;
    String imagenPiso;

    public int getIdPiso() {
        return idPiso;
    }

    public void setIdPiso(int idPiso) {
        this.idPiso = idPiso;
    }

    public String getNumPiso() {
        return numPiso;
    }

    public void setNumPiso(String numPiso) {
        this.numPiso = numPiso;
    }

    public String getImagenPiso() {
        return imagenPiso;
    }

    public void setImagenPiso(String imagenPiso) {
        this.imagenPiso = imagenPiso;
    }
}
